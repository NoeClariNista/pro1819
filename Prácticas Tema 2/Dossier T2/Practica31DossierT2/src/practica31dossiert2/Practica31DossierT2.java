    public static void main(String[] args) {
        
        /* Práctica 31: Hacer un programa que muestre la tabla de multiplicar del número 5 usando
        un bucle for. */
        
        int tabla = 5;
        for (int i = 1;i < 11;i++){
            System.out.println(tabla +"x"+ i +"="+ (tabla*i));
        }
        
    }
    
